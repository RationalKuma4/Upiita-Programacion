public class Animales 
{
    String raza;
    String nombre;
    int patas;
    boolean carnivoro;
    boolean hervivioro;
    
    Animales()
    { }
    
    Animales(String rAZA, String nOMBRE, int pATAS, boolean hERVIVORO, boolean cARNIVORO)
    {
        this.raza = rAZA;
        this.nombre = nOMBRE;
        this.patas = pATAS;
        this.carnivoro = cARNIVORO;
        this.hervivioro = hERVIVORO;
    }
}
