    //Depositar
    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        double monto = Double.parseDouble(jTextField1.getText());
        //Operacion Auxiliar
        double saldoAuxiliar = cuenta.saldo;
        saldoAuxiliar = saldoAuxiliar + monto;
        
        if(saldoAuxiliar < 5000)
        {
            cuenta.saldo = cuenta.saldo + monto;
            jTextField3.setText(Double.toString(cuenta.saldo));
            jLabel5.setVisible(false);
        }
        else if(saldoAuxiliar > 5000)
        {
            jLabel5.setVisible(true);
            jLabel5.setText("No es posible tener depositado mas de $5000. Ajuste su deposito.");
            buttonGroup1.clearSelection();
        }
    }//GEN-LAST:event_jRadioButton1ActionPerformed
    
    //Retirar
    private void jRadioButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton2ActionPerformed
        double monto = Double.parseDouble(jTextField1.getText());
        //Operacion Auxiliar
        double saldoAuxiliar = cuenta.saldo;
        saldoAuxiliar = saldoAuxiliar - monto;
        
        if(saldoAuxiliar >= 100)
        {
            cuenta.saldo = cuenta.saldo - monto;
            jTextField3.setText(Double.toString(cuenta.saldo));
            jLabel5.setVisible(false);
        }
        else if(saldoAuxiliar < 100)
        {
            jLabel5.setVisible(true);
            jLabel5.setText("No puede dejar su cuenta vacia. Ajuste su deposito.");
            buttonGroup1.clearSelection();
        }
    }//GEN-LAST:event_jRadioButton2ActionPerformed
    
