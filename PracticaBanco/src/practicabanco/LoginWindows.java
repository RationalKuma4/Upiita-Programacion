    Usuario BuscaUsuarioCuenta(String tbUsuario)
    {
        int i = 0;
        Usuario usuar = null;
        
        for(Usuario us : oUsuario)
        {
            if(us.usuario.equals(tbUsuario))
            {
                usuar = us;
                indice = i;
                break;
            }
            i++;
        }
        
        for(Cuenta con : oCuenta)
        {
            if(usuar.id == con.clienteid)
            {
                cuenta = con;
                break;
            }
        }
        
        return usuar;
    }
    
