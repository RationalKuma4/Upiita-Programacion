    //Borra Palabra
    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int idpalabra = Integer.parseInt(jTextField3.getText());
        if(!jTextField3.getText().isEmpty())
        {
            boolean fueExitoso = false;
            Diccionario palabra = new Diccionario();
            fueExitoso = palabra.EliminaPalabra(idpalabra);
            
            if(fueExitoso)
            JOptionPane.showMessageDialog(jPanel3, "Correcto.");
            else
                JOptionPane.showMessageDialog(jPanel3, "Error.");
            jPanel3.setVisible(false);
            jTextField3.setText("");
        }
        else if(jTextField3.getText().isEmpty())
            JOptionPane.showMessageDialog(jPanel3, "Introduzca el id de la palabra.");
    }//GEN-LAST:event_jButton7ActionPerformed
