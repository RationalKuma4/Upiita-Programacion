    //AgregarPalabra // O Modifica palabra
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(esRegistro)
        {
            String palabra = jTextField1.getText();
            boolean fueExitoso = false;
            if(!palabra.isEmpty())
            {
                double calificacion = Double.parseDouble(jTextField2.getText());
                Diccionario nuevaPalabra = new Diccionario();
                fueExitoso = nuevaPalabra.GuardaPalabra(palabra, calificacion);

                if(fueExitoso)
                JOptionPane.showMessageDialog(jPanel2, "Registro Correcto.");
                else
                    JOptionPane.showMessageDialog(jPanel2, "Registro Incorrecto.");
                jPanel2.setVisible(false);
                jTextField1.setText("");
                jTextField2.setText("");
            }
            else if(palabra.isEmpty())
            {
                JOptionPane.showMessageDialog(jPanel2, "Introduzca la palabra.");
            }
        }
        else if(!esRegistro)
        {
            double calificacion = Double.parseDouble(jTextField2.getText());
            Diccionario modificapa = (new Diccionario()).GetOnePalbra(palabraId);
            modificapa.calificacion = calificacion;
            boolean fueExitoso = modificapa.ModificaPalbra(modificapa);
            if(fueExitoso)
                JOptionPane.showMessageDialog(jPanel2, "Correcto.");
                else
                    JOptionPane.showMessageDialog(jPanel2, "Incorrecto.");
            jPanel2.setVisible(false);
            jTextField1.setText("");
            jTextField2.setText("");
            jButton5.setText("Agregar");
        }
    }//GEN-LAST:event_jButton5ActionPerformed
