    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Establecimiento estaConsulta[] = null;
        estaConsulta = archivo.LeerEstablecimiento(rutaArchivos + archivoEstablecimientos);
        String establecimiento = "";
        for(Establecimiento es : estaConsulta)
            establecimiento = establecimiento + es.InformacionEstablecimiento();
        
        JOptionPane.showMessageDialog(jPanel1, establecimiento);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = jTextField1.getText();
        
        if(!nombre.isEmpty())
        {
            String giro = jTextField2.getText();
            String direccion = jTextField3.getText();
            int nuevoid = estableci.size() + 1;
            
            Establecimiento estab = new Establecimiento();
            estab.idEstablecimiento = nuevoid;
            estab.nombre = nombre;
            estab.giro = giro;
            estab.direccion = direccion;
            estab.comentarios = 0;
            estab.calificacion = 0;
                    
            estableci.add(estab);
            Establecimiento[] array = new Establecimiento[estableci.size()];
            GuardaEstablecimientos(estableci.toArray(array));
            
            JOptionPane.showMessageDialog(jPanel1, "Registro Correcto.");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
        }
        else if(nombre.isEmpty())
        {
            JOptionPane.showMessageDialog(jPanel1, "Introduzca el nombre.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

