    private void jPasswordField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPasswordField1ActionPerformed
        String jUsuario = jTextField1.getText();
        String jContra = jPasswordField1.getText();
        
        if(jUsuario.equals(usuario) && jContra.equals(contra))
        {
            jLabel4.setVisible(true);
            jTextField2.setVisible(true);
            jTextField1.setEnabled(false);
            jPasswordField1.setEnabled(false);
            jButton1.setVisible(true);
        }
        else
        {
            if(conteoIntentos > 1)
            {
                conteoIntentos--;
                jLabel5.setText("Usurio y/o contrase√±a incorrectos. Tienes " + conteoIntentos + " restantes.");
                jLabel5.setVisible(true);
            }
            else if(conteoIntentos == 1)
            {
                jTextField1.setEnabled(false);
                jPasswordField1.setEnabled(false);
                jButton1.setEnabled(true);
                JOptionPane.showMessageDialog(jPanel1, "Intentos agotados. Intente mas tarde.", "Error", JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        }
    }//GEN-LAST:event_jPasswordField1ActionPerformed

